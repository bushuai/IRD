package me.donkey.entity;

public class Car {
	private int id;
	private int roomId;
	private int customerId;
	private String car_position_num;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getRoomId() {
		return roomId;
	}

	public void setRoomId(int roomId) {
		this.roomId = roomId;
	}

	public int getCustomerId() {
		return customerId;
	}

	public void setCustomerId(int customerId) {
		this.customerId = customerId;
	}

	public String getCar_position_num() {
		return car_position_num;
	}

	public void setCar_position_num(String car_position_num) {
		this.car_position_num = car_position_num;
	}



}
